                    removing screws or latches.
                </h3>
                <h3><span class="box2">
                        Step 3: Clean the Interior</span>
                    <div class="spacer_shape">
                        - <a href="../hardware/?chassis#case">Inside the case</a>, you'll find various components that may
                        need cleaning:<br /><br />
                        - <a href="../hardware/?cooling#cooler">Fans:</a> Dust tends to accumulate on CPU and GPU fans. Use
                        compressed air to blow out the dust from the fans, holding them in place to prevent them from
                        spinning while cleaning.<br /><br />
                        - <a href="../hardware/?motherboard#board">Motherboard:</a> Use compressed air to remove dust from
                        the motherboard, but avoid touching the sensitive components.<br /><br />
                        - <a href="../hardware/?gpu#gpu">Graphics Card (GPU):</a> If you have a dedicated graphics card,
                        clean it in the same way as the motherboard.<br /><br />
                        - <a href="../hardware/?cables_connectors#top">Cables:</a> Check for loose or disconnected cables
                        and reattach them securely.<br /><br />
                        - <a href="../hardware/?psu#psu">Power Supply Unit (PSU):</a> Clean the PSU fan and vents with
                        compressed air.
                    </div>
                </h3>
