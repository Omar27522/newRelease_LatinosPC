            <ruby>Matthew<rt>14:22-33</rt></ruby>
        </small></a></h3>
    <p>
        One of the most famous stories of Peter's impulsiveness and faith is when he walked on
        water immediately after the
        <a href="https://en.wikipedia.org/wiki/Feeding_the_multitude#:~:text=The%20Feeding%20of%20the%205%2C000%20is%20also%20known%20as%20the,boy%20to%20feed%20a%20multitude."
