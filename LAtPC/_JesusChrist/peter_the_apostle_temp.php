  <h2 class="total-count">The Council of Jerusalem <small><a href="https://ebible.org/study/?v1=AC15_1&w1=bible&t1=local%3Aeng-web" title="Acts 15" style="color:var(--dialog-text-highlight)">Acts 15</a></small></h2>
