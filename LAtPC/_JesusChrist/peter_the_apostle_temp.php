        <hr style="margin-top:0;"><a href="?james#^">James</a> and <a href="?john#^">John</a> were fellow fishermen and friends of Simon.</h3>
        <p>Andrew couldn’t stop talking about the man John had baptized in the <a href="https://en.wikipedia.org/wiki/Jordan_River"title="Wikipedia">Jordan River</a>, and how the sky seemed to
            open when he came up from the water. Andrew said a voice came from heaven and said,<br /> <strong>“This is my Son, whom I
            love. I am very pleased with him.”</strong>
            <small><a href="https://ebible.org/study/?t1=local%3Aeng-web&w1=bible&v1=MT3_16"title="Matthew 3:16-17"><ruby>Matthew<rt>3:16-17</rt></ruby></a></small>
            <small><a href="https://ebible.org/study/?v1=MK1_9&w1=bible&t1=local%3Aeng-web"title="Mark 3:9-11"><ruby>Mark<rt>3:9-11</rt></ruby></a></small>
            <small><a href="https://ebible.org/study/?v1=LK3_22&w1=bible&t1=local%3Aeng-web"title="Luke 3:22"><ruby>Luke<rt>3:22</rt></ruby></a></small>
            <small><a href="https://ebible.org/study/?v1=JN1_29&w1=bible&t1=local%3Aeng-web"title="John 1:29-34"><ruby>John<rt>1:29-34</rt></ruby></a></small>
        </p>

        <p>He told Simon about a man by the river. A man who stepped into the water. A man Andrew believed was sent by
            God. Simon listened, but his hands stayed busy. Nets didn’t mend themselves. That morning, Simon was tired. Very tired.</p>
        <p>He and his brother Andrew had been fishing all night. Their partners, James and John, were with them too.
            They tried every place they knew on the lake. When morning came, they had caught nothing.
            Now the men stood by the shore, washing their empty nets.
            A crowd began to gather nearby. Andrew looked up and whispered, “Simon, that’s him. His name is Jesus.”</p>
        <p>Jesus walked toward Simon’s boat. “<strong><a href="https://biblehub.com/luke/5-3.htm#topheading"title="Luke 5:3">May I use your boat?</a></strong>” Jesus asked.<br />
            Simon nodded, and pushed the boat a little way out from shore and sat inside while Jesus spoke to the people.
            His voice carried across the water, the crowd grew quiet and was listening. When Jesus finished teaching, he turned to Simon.</p>
        <p>Jesus said “<a href="https://biblehub.com/luke/5-4.htm#topheading"title="Luke 5:4"><strong>Go out into deeper water and let down your nets.</strong></a>” Simon shook his head.
            “<strong>Master</strong>,” he said, “<a href="https://biblehub.com/luke/5-5.htm#topheading"title="Luke 5:5"><strong>we worked all night and caught nothing.</strong></a>”
            fishermen knew night was the best time to catch fish, daytime was not.
            <br />But then Simon said, “<a href="https://biblehub.com/luke/5-5.htm#topheading"title="Luke 5:5"><strong>If you say so, I will let down the nets.</strong></a>”</p>
        <p>The nets went into the water.</p>
        <p>Suddenly, they pulled hard.</p>
        <p>Fish filled them—too many. The nets began to tear. Simon shouted to James and John to help. Their boat rushed over.
            Fish poured in until both boats were so full they nearly sank. Simon stared at the fish, then he looked at Jesus — fell to his knees and said:
            <a href="https://ebible.org/study/?t1=local%3Aeng-web&w1=bible&v1=LK5_8"title="Luke 5:8"><strong>"Go away from me, Lord; I am a sinful man!"</strong></a>
            The lake had never done this before.</p>
        <p>Jesus looked at Simon, Andrew, James, and John and said they did not need to be afraid.
            Then he told Simon and his helpers to not be afraid from now own they would be catching men.
            Then they left the everything and followed Jesus.</p>
