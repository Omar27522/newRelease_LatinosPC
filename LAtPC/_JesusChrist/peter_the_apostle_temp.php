    <h3>Name, Identity, and Social Position</h3>
    <p>Simon (Σίμων) was one of the most common male names in Judea and Galilee in the early 1st century. It suggests nothing remarkable — which is precisely the point. He was not
      born into distinction.</p>
    <p>“Bar Jonah” identifies him as his father’s son, likely a man known locally but not widely. This is an identity rooted in family and village reputation, not personal
      achievement.</p>
    <ul>
      <li>
        <h4>Simon would have been known in Capernaum not as an individual visionary, but as:</h4> A working man, a son and brother, a provider, and a familiar face in the harbor
        and marketplace
      </li>
    </ul>
    <h3 style="margin-top: 3.2%;">Family Structure & Responsibilities</h3>
    <p>Simon was part of a family-based fishing enterprise, not a lone laborer.</p>
    <section class="homepage-grid">
      <ul>
        <h4>Likely Features:</h4>
        <li>His father had been a fisherman before him</li>
        <li>Skills passed down, not learned in school</li>
        <li><a href="?andrew#^" title="Andrew the Apostle">Andrew</a> worked alongside him</li>
        <li>Extended family likely lived nearby</li>
      </ul>
      <ul>
        <h4>Simon was probably:</h4>
        <li>Married by his late teens or early twenties</li>
        <li>Responsible for a household</li>
        <li>Accustomed to shared labor and mutual dependence</li>
      </ul>
      <ul>
        <h4>This would shape him into:</h4>
        <li>Practical</li>
        <li>Direct</li>
        <li>Protective</li>
        <li>Accustomed to command and response</li>
      </ul>
    </section>
    <h3>The Fishing Trade: Daily Reality</h3>
    <p>Fishing was dangerous, regulated, and exhausting.</p>
    <section class="homepage-grid">
      <ul>
        <h4>Economic Reality:</h4>
        <li>Fishing rights were leased</li>
        <li>Taxes paid on catch, boats, and equipment</li>
        <li>Income fluctuated with weather, season, and Roman enforcement</li>
        <li>Worked nights</li>
        <li>Slept irregular hours</li>
      </ul>
      <ul>
        <h4>Simon likely:</h4>
        <li>Had calloused hands and weathered skin</li>
        <li>Knew the lake intimately</li>
        <li>Had physical confidence</li>
        <li>Had low tolerance for abstract speculation</li>
      </ul>
